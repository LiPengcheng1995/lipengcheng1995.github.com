
$(".new").mouseenter(function(){
	$(".left").animate({left:'0',opacity:.8},200)
	$(".right").animate({right:'0',opacity:.8},200)
});

$(".new").mouseleave(function(){
	$(".left").animate({left:'-73px',opacity:0},200)
	$(".right").animate({right:'-73px',opacity:0},200)
});
